static const char *TAG = "genvexv3.climate";

void Genvexv3Climate::setup() {
  current_temp_sensor_->add_on_state_callback([this](float state) {
    ESP_LOGD(TAG, "CURRENT TEMP SENSOR CALLBACK: %f", state);
    current_temperature = state;
    publish_state();
  });
  temp_setpoint_number_->add_on_state_callback([this](float state) {
    ESP_LOGD(TAG, "TEMP SETPOINT SENSOR CALLBACK: %f", state);
    target_temperature = state;
    publish_state();
  });
  fan_speed_number_->add_on_state_callback([this](float state) {
    ESP_LOGD(TAG, "FAN SPEED SENSOR CALLBACK: %f", state);
    genvexv3fanspeed_to_fanmode(state);
    publish_state();
  });

  current_temperature = current_temp_sensor_->state;
  target_temperature  = temp_setpoint_number_->state;
  genvexv3fanspeed_to_fanmode(fan_speed_number_->state);
}

void Genvexv3Climate::control(const climate::ClimateCall& call) {
  if (call.get_target_temperature().has_value())
  {
    this->target_temperature = *call.get_target_temperature();
    float target = target_temperature;
    ESP_LOGD(TAG, "Target temperature changed to: %f", target);
    temp_setpoint_number_->make_call().set_value(target).perform();//set(target);
  }

  if (call.get_mode().has_value())
  {
    ESP_LOGD(TAG, "Mode changed from Home Assistant");
    auto new_mode = *call.get_mode();
    mode = new_mode;
    switch (mode) {
      case climate::CLIMATE_MODE_OFF: 
      {
        ESP_LOGD(TAG, "Mode changed to OFF");
        this->fan_mode = climate::CLIMATE_FAN_OFF;
        // Map OFF to fan speed 0
        fan_speed_number_->make_call().set_value(0).perform();//set(0);
        break;
      }
      case climate::CLIMATE_MODE_AUTO: 
      {
        ESP_LOGD(TAG, "Mode changed to AUTO");
        // Default AUTO to speed 2
        fan_mode.reset();
        fan_speed_number_->make_call().set_value(2).perform();
        break;
      }
      default:
        break;
    }
  }
  // Map Home Assistant fan mode selections
  if (call.get_fan_mode().has_value()) {
    auto new_fan_mode = *call.get_fan_mode();
    if (new_fan_mode == climate::ClimateFanMode::CLIMATE_FAN_OFF) {
      ESP_LOGD(TAG, "Fan mode OFF selected");
      fan_speed_number_->make_call().set_value(0).perform();
      this->mode = climate::CLIMATE_MODE_OFF;
      this->fan_mode = climate::CLIMATE_FAN_OFF;
    }
  }

  this->publish_state();
}

climate::ClimateTraits Genvexv3Climate::traits() {
  auto traits = climate::ClimateTraits();

  traits.set_supported_custom_fan_modes({
    "1",
    "2",
    "3",
    "4"
  });

  traits.set_supported_fan_modes({ 
    climate::ClimateFanMode::CLIMATE_FAN_OFF 
  });

  traits.set_supported_modes({
    climate::ClimateMode::CLIMATE_MODE_OFF,
    climate::ClimateMode::CLIMATE_MODE_AUTO,
   });

  traits.add_feature_flags(climate::CLIMATE_SUPPORTS_CURRENT_TEMPERATURE);
  traits.set_visual_temperature_step(0.1);
  traits.set_visual_min_temperature(5);
  traits.set_visual_max_temperature(30);

  return traits;
}

void Genvexv3Climate::dump_config() {
  LOG_CLIMATE("", "Genvexv3 Climate", this);
}

void Genvexv3Climate::genvexv3fanspeed_to_fanmode(const int state)
{
  ESP_LOGD(TAG, "In genvexv3fanspeed_to_fanmode");
  climate::ClimateFanMode return_value;
  ESP_LOGD(TAG, "State is %i", state);
  this->fan_mode.reset();

  switch (state) {
  case 1:
    ESP_LOGD(TAG, "Case 1");
    this->mode = climate::CLIMATE_MODE_AUTO;
    break;
  case 2:
    ESP_LOGD(TAG, "Case 2");
    this->mode = climate::CLIMATE_MODE_AUTO;
    break;
  case 3:
    ESP_LOGD(TAG, "Case 3");
    this->mode = climate::CLIMATE_MODE_AUTO;
    break;
  case 4:
    ESP_LOGD(TAG, "Case 4");
    this->mode = climate::CLIMATE_MODE_AUTO;
    break;
  case 0:
    ESP_LOGD(TAG, "Case 0");
    this->fan_mode = climate::CLIMATE_FAN_OFF; 
    this->mode = climate::CLIMATE_MODE_OFF;
    break;
  default: 
    ESP_LOGD(TAG, "Case default");
    this->fan_mode = climate::CLIMATE_FAN_OFF; 
    this->mode = climate::CLIMATE_MODE_OFF;
    break;
  }
}

int Genvexv3Climate::climatemode_to_genvexv3operationmode(const climate::ClimateMode mode) {
  switch (mode) {
    case climate::CLIMATE_MODE_OFF:
      return 0;
    case climate::CLIMATE_MODE_AUTO:
      return 2; // default mapping
    default:
      return 0;
  }
}

void Genvexv3Climate::genvexv3modetext_to_climatemode(const std::string& genvexv3_mode) {
  if (genvexv3_mode == "OFF") {
    this->mode = climate::CLIMATE_MODE_OFF;
  } else {
    this->mode = climate::CLIMATE_MODE_AUTO;
  }
}

